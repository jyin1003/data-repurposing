#!/usr/bin/env node
/**
 * generate-manifest.js
 * --------------------
 * Scans datasets/*.json and writes datasets/datasets-data.js — a plain JS file
 * that assigns all dataset data to a global variable.
 *
 * index.html loads it via <script src="datasets/datasets-data.js">, which works
 * when opening the file directly in any browser (no server needed).
 *
 * Run whenever you add or remove a dataset:
 *   node generate-manifest.js
 *
 * The pre-commit hook runs this automatically.
 */

const fs = require('fs');
const path = require('path');

const datasetsDir = path.join(__dirname, 'datasets');
const outputPath = path.join(datasetsDir, 'datasets-data.js');

if (!fs.existsSync(datasetsDir)) {
  console.error('datasets/ directory not found. Run from the repo root.');
  process.exit(1);
}

const files = fs.readdirSync(datasetsDir)
  .filter(f => f.endsWith('.json') && f !== 'manifest.json' && !f.startsWith('_'))
  .sort();

const datasets = files.map(f => {
  const filepath = path.join(datasetsDir, f);
  try {
    return JSON.parse(fs.readFileSync(filepath, 'utf8'));
  } catch (e) {
    console.error(`  ✗ Could not parse ${f}: ${e.message}`);
    process.exit(1);
  }
});

const output = `// Auto-generated by generate-manifest.js — do not edit by hand.\n// Run: node generate-manifest.js\nwindow.DATASETS_DATA = ${JSON.stringify(datasets, null, 2)};\n`;

fs.writeFileSync(outputPath, output);
console.log(`✓ datasets-data.js updated with ${datasets.length} dataset(s):\n  ${files.join('\n  ')}`);
