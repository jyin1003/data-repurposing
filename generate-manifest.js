#!/usr/bin/env node
/**
 * generate-manifest.js
 * --------------------
 * Scans papers/*.json and writes papers/papers-data.js — a plain JS file
 * that assigns all paper data to a global variable.
 *
 * index.html loads it via <script src="papers/papers-data.js">, which works
 * when opening the file directly in any browser (no server needed).
 *
 * Run whenever you add or remove a paper:
 *   node generate-manifest.js
 *
 * The pre-commit hook runs this automatically.
 */

const fs   = require('fs');
const path = require('path');

const papersDir  = path.join(__dirname, 'papers');
const outputPath = path.join(papersDir, 'papers-data.js');

if (!fs.existsSync(papersDir)) {
  console.error('papers/ directory not found. Run from the repo root.');
  process.exit(1);
}

const files = fs.readdirSync(papersDir)
  .filter(f => f.endsWith('.json') && f !== 'manifest.json' && !f.startsWith('_'))
  .sort();

const papers = files.map(f => {
  const filepath = path.join(papersDir, f);
  try {
    return JSON.parse(fs.readFileSync(filepath, 'utf8'));
  } catch(e) {
    console.error(`  ✗ Could not parse ${f}: ${e.message}`);
    process.exit(1);
  }
});

const output = `// Auto-generated by generate-manifest.js — do not edit by hand.\n// Run: node generate-manifest.js\nwindow.PAPERS_DATA = ${JSON.stringify(papers, null, 2)};\n`;

fs.writeFileSync(outputPath, output);
console.log(`✓ papers-data.js updated with ${papers.length} paper(s):\n  ${files.join('\n  ')}`);
